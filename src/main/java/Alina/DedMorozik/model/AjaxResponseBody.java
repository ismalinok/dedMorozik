package Alina.DedMorozik.model;

public class AjaxResponseBody {


    String behaviorMsg = "";
    String msg = "";
    String produceMsg = "";

    public void setBehaviorMsg(String behaviorMsg) {
        this.behaviorMsg = behaviorMsg;
    }

    public String getBehaviorMsg() {
        return behaviorMsg;
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }

    public String getProduceMsg() {
        return produceMsg;
    }

    public void setProduceMsg(String produceMsg) {
        this.produceMsg = produceMsg;
    }


}